package com.ncu.mfc.model;

public class Level {

    private String fails;

    private String good;

    private String excellence;

    private String avgGra;

    private String minGra;

    private String maxGra;

    public String getFails() {
        return fails;
    }

    public void setFails(String fails) {
        this.fails = fails;
    }

    public String getGood() {
        return good;
    }

    public void setGood(String good) {
        this.good = good;
    }

    public String getExcellence() {
        return excellence;
    }

    public void setExcellence(String excellence) {
        this.excellence = excellence;
    }

    public String getAvgGra() {
        return avgGra;
    }

    public void setAvgGra(String avgGra) {
        this.avgGra = avgGra;
    }

    public String getMinGra() {
        return minGra;
    }

    public void setMinGra(String minGra) {
        this.minGra = minGra;
    }

    public String getMaxGra() {
        return maxGra;
    }

    public void setMaxGra(String maxGra) {
        this.maxGra = maxGra;
    }
}
